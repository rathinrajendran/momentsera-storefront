const ROMANTIC_ACCENTS: AccentKey[] = ["blush", "rosewood", "plum", "lavender", "orchid", "champagne", "gold", "ivory"];
const LUXURY_ACCENTS: AccentKey[] = ["gold", "champagne", "ivory", "charcoal", "wine", "copper", "mocha", "pearl"];
/* =========================================================
   THEME ACCENT RULES
========================================================= */
export const THEME_ACCENT_RULES: Record<ThemeKey, AccentKey[]> = {
  "Rose & Cream": ROMANTIC_ACCENTS,

  "Navy & Gold": LUXURY_ACCENTS,

  "Sky Blue": LIGHT_ACCENTS,

  "Blush Pink": ROMANTIC_ACCENTS,

  "Mint Green": NATURE_ACCENTS,

  "Warm Ivory": LUXURY_ACCENTS,
};
/* =========================================================
   HELPERS
========================================================= */
export function normalizeAccent(theme: ThemeKey, accent: AccentKey): AccentKey {
  const safeTheme: ThemeKey = theme in THEME_ACCENT_RULES ? theme : "Rose & Cream";

  const allowed = THEME_ACCENT_RULES[safeTheme];

  if (!allowed?.length) {
    return "sage";
  }

  return allowed.includes(accent) ? accent : allowed[0];
}

/* =========================================================
   BORDER RADIUS
========================================================= */

export function getThemeRadius(radius?: "sharp" | "soft" | "rounded" | "pill") {
  switch (radius) {
    case "sharp":
      return "0px";

    case "soft":
      return "6px";

    case "rounded":
      return "20px";

    case "pill":
      return "999px";

    default:
      return "20px";
  }
}
